    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {
            connect();
        } catch (ClassNotFoundException ex) {            
        } catch (SQLException ex) {}
        
        // Variables to be introduced
        String  isbn       = jTextField1.getText();
        String  titulo     = jTextField2.getText();
        String  autor      = jTextField3.getText();
        String  editorial  = jTextField4.getText();
        String  tema       = jTextField5.getText();
        String  ano        = jTextField6.getText();
        
        // Sql sentence, "?" will be sustitued
        PreparedStatement smt = null;
        try {
            smt = conn.prepareStatement("INSERT INTO libro VALUES (?, ?, ?, ?, ?, ?)");
        } catch (SQLException ex) {}
        
        try {
            // Sustitutes the "?" with variables
            smt.setString(1, isbn);
            smt.setString(2, titulo);
            smt.setString(3, autor);
            smt.setString(4, editorial);
            smt.setString(5, tema);
            smt.setString(6, ano);
            smt.executeUpdate();
        } catch (SQLException ex) {/*LOG*/System.out.println("ERROR setString");}
        
    }//GEN-LAST:event_jButton1ActionPerformed

